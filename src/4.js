let arr = []
let arrRecove = []

let coppiaUguali = -1
let coppiaDiversi = -1

let isFixed = true;
let sx = 0
let dx = B - 1;

function setUp() {
    if (debug) console.log("SETUP");
    arr = []
    arrRecove = []
    for (let i = 0; i < B; i++) {
        arr.push(-1)
    }
    coppiaUguali = -1
    coppiaDiversi = -1
    isFixed = true;
    sx = 0
    dx = B - 1;
    counter = 0;
    asked = -1;
    askedToRecover = false;
    finio = false;
    fastRecoveryToAks = []

    asked = simpleAsk()
    console.log(asked + 1)
    counter++;
}

function simpleAnswer(asked, line) {
    if (debug) console.log("simpleAnswer", asked);
    if (asked !== -1)
        arr[asked] = +line;

    if (arr[reciproco(asked)] !== -1) {
        if (arr[asked] === arr[reciproco(asked)]) {
            coppiaUguali = asked
            if (debug) console.log("COPPIA UGUALI", asked);

        } else {
            coppiaDiversi = asked
            if (debug) console.log("COPPIA DIVERSI", asked);

        }
    }

    if (sx > dx) {
        finito()
    }
}

function simpleAsk() {
    if (debug) console.log("simpleAsk");
    //ALTERNA
    if (sx > B - 1 - dx) {
        dx--;
        return dx + 1;
    } else {
        sx++
        return sx - 1;
    }
}

