let fastRecoveryToAks = []

function setFastRecovery() {
    if (debug) console.log("setFastRecovery");

    if (coppiaDiversi !== -1)
        fastRecoveryToAks.push(coppiaDiversi)
    if (coppiaUguali !== -1)
        fastRecoveryToAks.push(coppiaUguali)
    for (let i = 0; i < B; i++) {
        arrRecove.push(-1)
    }
}

function fastRecovery(asked, answer) {
    if (debug) console.log("fastRecovery");
    if (debug) console.log("coppiaUguali", coppiaUguali);
    if (debug) console.log("coppiaDiversi", coppiaDiversi);
    arrRecove[asked] = +answer;
    if (fastRecoveryToAks.length === 0)
        finishRecovery()
}

function finishRecovery() {
    if (debug) console.log("finishRecovery");
    if (debug) console.log(arr);
    if (debug) console.log(arrRecove);
    if (debug) console.log(coppiaUguali);
    if (debug) console.log(coppiaDiversi);

    if (coppiaDiversi === -1) { //TUTTI UGUALI

        if (arr[coppiaUguali] !== arrRecove[coppiaUguali]) {
            if (debug) console.log("REVERSE UGUALI");
            arr = arr.map(a => a === -1 ? -1 : (a === 1 ? 0 : 1));
        }
        if (sx > B - 1 - dx)
            sx--;
        else if (sx < B - 1 - dx)
            dx++;

    } else if (coppiaUguali === -1) { //TUTTI DIVERSI

        if (arr[coppiaDiversi] !== arrRecove[coppiaDiversi]) {
            if (debug) console.log("REVERSE DIVERSI");
            arr = arr.reverse()
        }

        if (sx > B - 1 - dx)
            sx--;
        else if (sx < B - 1 - dx)
            dx++;

    } else {

        if (arr[coppiaUguali] === arrRecove[coppiaUguali]) { // REVERT || NOTHIN
            if (arr[coppiaDiversi] === arrRecove[coppiaDiversi]) { //NOTHING
                if (debug) console.log("NOTHING");;
            } else { //REVERT
                if (debug) console.log("REVERSE");
                arr = arr.reverse();
            }
        } else { //FLIP || REVRT & FLIP
            if (arr[coppiaDiversi] !== arrRecove[coppiaDiversi]) { //FLIP
                if (debug) console.log("FLIP");
                arr = arr.map(a => a === -1 ? -1 : (a === 1 ? 0 : 1));
            } else { //REVERT & FLIP
                if (debug) console.log("FLIP REVERTE");
                arr = arr.map(a => a === -1 ? -1 : (a === 1 ? 0 : 1)).reverse();
            }
        }


        sx = 0;
        while (sx < arr.length && arr[sx] !== -1)
            sx++;
        dx = B - 1;
        while (dx >= 0 && arr[dx] !== -1)
            dx--;
    }

    arrRecove = [];
}




function reciproco(i) {
    return B - 1 - i;
}




